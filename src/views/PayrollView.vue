<script>
export default {
  data() {
    return {
      currentIndex: 0,
      testimonialCount: 0,
      testimonialContainerWidth: 0,
      testimonialListWidth: 0,
    };
  },
  methods: {
    moveLeft() {
      if (this.currentIndex > 0) {
        this.currentIndex--;
        this.scrollTestimonialList();
      }
    },
    moveRight() {
      if (this.currentIndex < this.testimonialCount - 1) {
        this.currentIndex++;
        this.scrollTestimonialList();
      }
    },
    scrollTestimonialList() {
      const scrollPosition =
        this.currentIndex * (this.testimonialListWidth / this.testimonialCount);
      this.$refs.testimonialList.style.transform = `translateX(-${scrollPosition}px)`;
    },
  },
  mounted() {
    this.testimonialCount = this.$refs.testimonialList.children.length;
    this.testimonialContainerWidth =
      this.$refs.testimonialContainer.offsetWidth;
    this.testimonialListWidth =
      this.testimonialCount * this.testimonialContainerWidth;
  },
};
</script>

