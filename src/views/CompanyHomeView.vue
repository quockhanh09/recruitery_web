<script>
export default {
  data() {
    return {
      companies: [
        // Danh sách các công ty - bạn cần cung cấp tên và liên kết
        // Ví dụ:
        { name: "f & EM Art Design", link: "#" },
        { name: "dCB", link: "#" },
        { name: "kdmattic", link: "#" },
        { name: "cFit", link: "#" },
        // Thêm các công ty khác...
      ],
      currentPage: 1,
      itemsPerPage: 2,
    };
  },
  computed: {
    groupedCompanies() {
      const sortedCompanies = this.companies.slice().sort((a, b) => {
        const nameA = a.name.toUpperCase();
        const nameB = b.name.toUpperCase();
        return nameA.localeCompare(nameB);
      });

      const groups = [];
      let currentLetter = "";

      sortedCompanies.forEach((company) => {
        const firstLetter = company.name[0].toUpperCase();
        if (firstLetter !== currentLetter) {
          groups.push({ letter: firstLetter, companies: [company] });
          currentLetter = firstLetter;
        } else {
          groups[groups.length - 1].companies.push(company);
        }
      });

      return groups;
    },
    totalPages() {
      return Math.ceil(this.groupedCompanies.length / this.itemsPerPage);
    },
  },
  methods: {
    changePage(pageNumber) {
      this.currentPage = pageNumber;
    },
  },
};
</script>

